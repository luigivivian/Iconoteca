        <h3 class="w3-center w3-margin-top">Núcleo de Pré-História e Arqueologia – NUPHA</h3>
    </div>
    <div class="w3-container w3-large">
        <p class="w3-opacity">
            O Núcleo de Pré-História e Arqueologia (NuPHA) possui formação multidisciplinar, congregando arqueólogos, historiadores, antropólogos de instituições nacionais e internacionais (Argentina, Venezuela e Uruguai). As linhas de investigação do Núcleo abrangem os seguintes temas: Pré-História do Planalto Meridional; Arqueologia missioneira; Educação patrimonial e difusão do conhecimento arqueológico; Arqueologia dos povos e comunidades tradicionais; Fronteiras e contato cultural interétnico na pré-história; Arqueologia histórica; Arqueologia forense; Patrimônio cultural, identidade e território; Pesquisa e curadoria de acervos arqueológicos; Estudos e guarda de acervos arqueológicos; Multidisciplinaridade: Arqueologia, Artes e Cultura.
        </p>
        <h4>Laboratório de Cultura Material e Arqueologia (LACUMA)</h4>
        <p class="w3-opacity">
            O NuPHA mantém um espaço físico específico no Instituto de Filosofia e Ciências Humanas para armazenamento e pesquisa de acervos arqueológicos, desenvolvimento de atividades educativas através de cursos, palestras e demais atividades de educação patrimonial. Em relação às atividades do LACUMA, a legislação brasileira referente aos processos de licenciamento ambiental compreende uma série de estudos voltados ao meio físico, biótico e socioeconômico. Entre os levantamentos realizados, destacam-se os estudos de avaliação de impactos ao patrimônio cultural material e imaterial.
        </p>
        <h4>Serviço</h4>
        <p class="w3-opacity">
Atua na preservação do material recebido por meio dos contratos de endosso e apoio institucional entre o NUPHA e empresas, além de órgãos públicos; do estudo e da conservação da cultura material e arqueológica; da formação de acervos; da limpeza de material iconográfico, lítico e cerâmico; da documentação gráfica e fotográfica; da descrição de artefatos arqueológicos; da catalogação dos acervos; do acondicionamento e armazenamento do material da cultura material e arqueológica; elaboração de laudos em temas de cultura material. Firma contratos com empresas para endossos de apoio institucional para projetos de diagnósticos arqueológicos e educação patrimonial, assume a guarda e organização de acervos de documentação material, elabora laudos, desenvolve eventos de cultura material e promove cursos e publicações que envolvem esses saberes.
        </p>

